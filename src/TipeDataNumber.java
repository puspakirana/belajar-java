//Tipe Data Number

public class TipeDataNumber {
    public static void main(String[] args) {

        //Integer Number
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 10000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000L;

        //Floating Point Number
        float iniFloat = 10.10F;
        double iniDouble = 10.10;

        //Literals Number
        int decimalInt = 25;
        int hexInt = 0xA132B; //0x(hexadecimal)
        int binInt = 0b101010 ; //0b(binary)

        //Underscore (ada sejak java 7/8), sebagai pemisah biar gampang bacanya
        long balance = 1_000_000_000_000L;
        int sum = 60_000_000;

    }
}
